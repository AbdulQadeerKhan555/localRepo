#Explicit type casting
        # The type casting done by the programmer or develper is explicit typeCating

#Type casting is a technique to convert the one data type into the other data type
a=1
b=3.5
c="4"
#Here c is a string but we can convert into the integer by using int() method
print(a+int(c))
#string() and float() method can also be used to convert other variable into to these

#Implicit typecasting
        # In this one datatype is converted into the other data type by Python (automatically)
d=9.4
e=5
# it is automatically converted into the float
print(d+e)